import 'package:flutter/material.dart';

class FontColor {
  Color errorColor() {
    return Color.fromARGB(255, 255, 22, 22);
  }

  Color generalTextBrightTheme() {
    return Colors.black;
  }

  Color postText() {
    return Color.fromARGB(255, 158, 158, 158);
  }

  Color textWithIcon() {
    return Color.fromARGB(255, 90, 90, 100);
  }

  Color tTextDark() {
    return Color.fromARGB(255, 90, 90, 100);
  }
}
